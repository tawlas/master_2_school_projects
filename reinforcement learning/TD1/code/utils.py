def render_policy(env, policy):
    """Helper to visualize policy on env.
    """
    state = env.reset()
    done = False

    env.render()
    print('\n')
    while not done:
        action = policy[state]
        state, r, done, _ = env.step(action)
        env.render()
        print('\n')

